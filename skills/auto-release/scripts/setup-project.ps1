      "$Stem-linux-x64.tar.gz", "$Stem-macos-x64.zip", "$Stem-macos-arm64.zip", "$Stem-web.zip"
    )
  }
  if ($Profile.ProjectType -eq "android") { return @("$Stem-$($Profile.Version).apk", "$Stem-$($Profile.Version).aab") }
