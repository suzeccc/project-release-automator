  }
  elseif ($Profile.ProjectType -eq "rust") {
    $readPattern = '(?ms)^\[package\]\s*(?:(?!^\[).)*?^version\s*=\s*"(?<version>\d+\.\d+\.\d+)"'
    $cargoPattern = '(?ms)(^\[package\]\s*(?:(?!^\[).)*?^version\s*=\s*")\d+\.\d+\.\d+(")'
    $updates += New-VersionUpdate "Cargo.toml" $cargoPattern '${1}{version}$2'
    $cargoName = [regex]::Escape($Profile.ProjectName)
    $lockPattern = '(?ms)(^\[\[package\]\]\s*(?:(?!^\[\[package\]\]).)*?^name\s*=\s*"' + $cargoName + '"\s*(?:(?!^\[\[package\]\]).)*?^version\s*=\s*")\d+\.\d+\.\d+(")'
    Add-ExistingVersionUpdate ([ref]$updates) "Cargo.lock" $lockPattern '${1}{version}$2'
    $commands += [pscustomobject][ordered]@{ name = "Tests"; command = "cargo test --all" }
    $commands += [pscustomobject][ordered]@{ name = "Package crate"; command = "cargo package --allow-dirty" }
    $artifacts += [pscustomobject][ordered]@{ source = "target/package/$Stem-{version}.crate"; sha256 = $true }
  }
  elseif ($Profile.ProjectType -eq "dotnet") {
    if ($Profile.NeedsVersionFile) {
      $readPattern = '(?m)^(?<version>\d+\.\d+\.\d+)\s*$'
      $updates += New-VersionUpdate "VERSION" '(?m)^\d+\.\d+\.\d+\s*$' '{version}'
      $additionalFiles += [pscustomobject]@{ RelativePath = "VERSION"; Content = "$($Profile.Version)`n" }
    }
    else {
      $element = [regex]::Escape([string]$Profile.VersionElement)
      $readPattern = "(?s)<$element>\s*(?<version>\d+\.\d+\.\d+)\s*</$element>"
      $updatePattern = "(?s)(<$element>\s*)\d+\.\d+\.\d+(\s*</$element>)"
      $updates += New-VersionUpdate $Profile.VersionSource $updatePattern '${1}{version}$2'
    }
    $bootstrapCommands += [pscustomobject][ordered]@{ name = "Restore"; command = "dotnet restore `"$($Profile.BuildPath)`"" }
    $bootstrapInputs += @($Profile.BuildPath, "packages.lock.json") | Where-Object {
      Test-Path -LiteralPath (Join-Path $script:ResolvedRepositoryRoot $_)
    }
    $projectDirectory = Split-Path -Parent $Profile.BuildPath
    $bootstrapRequiredPaths += if ($projectDirectory) { "$projectDirectory/obj/project.assets.json" } else { "obj/project.assets.json" }
    $commands += [pscustomobject][ordered]@{ name = "Tests"; command = "dotnet test `"$($Profile.BuildPath)`" --no-restore --configuration Release" }
    $commands += [pscustomobject][ordered]@{ name = "Pack"; command = "dotnet pack `"$($Profile.BuildPath)`" --no-restore --configuration Release -p:PackageVersion={version} --output dist" }
    $artifacts += [pscustomobject][ordered]@{ source = "dist/$Stem.{version}.nupkg"; sha256 = $true }
  }
  elseif ($Profile.ProjectType -eq "java") {
    $readPattern = $Profile.VersionReadPattern
    $updates += New-VersionUpdate $Profile.VersionSource $Profile.VersionUpdatePattern '${1}{version}$2'
    $commands += [pscustomobject][ordered]@{ name = "Test and package"; command = $Profile.JavaLocalBuild }
  }
  else {
    throw "No generation strategy for project type: $($Profile.ProjectType)"
  }

  if ($null -eq $localCommands) { $localCommands = @($commands) }

  $templateName = "$($Profile.ProjectType)-v1"
  $requiredAssets = @(Get-RequiredAssets $Profile.ProjectType $stem)
  $config = [pscustomobject][ordered]@{
    schemaVersion = 2
    projectType = $Profile.ProjectType
    automation = [pscustomobject][ordered]@{
      generator = $script:GeneratorName
      template = $templateName
      managedWorkflow = $ManagedWorkflow
      workflowFile = $SelectedWorkflowPath.Replace("\", "/")
    }
    projectName = $Profile.ProjectName
    branch = $defaults.Branch
    remote = $defaults.Remote
    tagPrefix = "v"
    version = [pscustomobject][ordered]@{
      read = [pscustomobject][ordered]@{ path = $readPath; pattern = $readPattern }
      updates = @($updates)
    }
    prepare = [pscustomobject][ordered]@{
      parallel = $false
      localOutputDirectory = "output"
      bootstrapInputs = @($bootstrapInputs | Sort-Object -Unique)
      bootstrapRequiredPaths = @($bootstrapRequiredPaths | Sort-Object -Unique)
      bootstrapCommands = @($bootstrapCommands)
      localCommands = @($localCommands)
      commands = @($commands)
      artifacts = @($artifacts)
    }
    releaseNotes = Get-ReleaseNotesConfig
    publish = [pscustomobject][ordered]@{
      workflow = Get-WorkflowConfig
      release = [pscustomobject][ordered]@{
        mode = "publish-draft"
        title = "{projectName} {tag}"
        requireDraft = $true
        requiredAssets = $requiredAssets
      }
    }
  }

  $expectedAssets = @(Get-ExpectedAssets $Profile $stem)
  $testStep = ""
  $buildStep = ""
  $managerSetup = ""
  $installCommand = "echo No package.json; skipping frontend dependencies"
  $pythonInstallCommand = "python -m pip install --upgrade build && python -m pip install -e ."
  $pythonBuildCommand = "python -m build"
  if ($Profile.Package) {
    $managerSetup = $Profile.Manager.WorkflowSetup
    $installCommand = $Profile.Manager.Install
    if (Test-PackageScript $Profile.Package "test") {
      $testStep = "      - name: Test`n        run: $(Get-PackageCommand $Profile.Manager.Name 'test')"
    }
    if (Test-PackageScript $Profile.Package "build") {
      $buildStep = "      - name: Build`n        run: $(Get-PackageCommand $Profile.Manager.Name 'build')"
    }
  }
  if ($Profile.ProjectType -eq "python") {
    if ($Profile.PackageManager -eq "uv") {
      $pythonInstallCommand = "python -m pip install uv && uv sync --all-extras"
      $pythonBuildCommand = "uv build"
      if ($Profile.HasTests) { $testStep = "      - name: Test`n        run: uv run pytest" }
    }
    elseif ($Profile.PackageManager -eq "poetry") {
      $pythonInstallCommand = "python -m pip install poetry && poetry install"
      $pythonBuildCommand = "poetry build"
      if ($Profile.HasTests) { $testStep = "      - name: Test`n        run: poetry run pytest" }
    }
    elseif ($Profile.HasTests) {
      $testStep = "      - name: Test`n        run: python -m pip install pytest && python -m pytest"
    }
  }
  $tokens = @{
    PROJECT_NAME = $Profile.ProjectName.Replace('"', '')
    ARTIFACT_STEM = $stem
    BUILD_PATH = [string]$Profile.BuildPath
    PACKAGE_MANAGER_SETUP = $managerSetup
    INSTALL_COMMAND = $installCommand
    TEST_STEP = $testStep
    BUILD_STEP = $buildStep
    PYTHON_INSTALL_COMMAND = $pythonInstallCommand
    PYTHON_BUILD_COMMAND = $pythonBuildCommand
    DOTNET_PROJECT = [string]$Profile.BuildPath
    DOTNET_VERSION = [string](Get-OptionalProperty $Profile "DotNetVersion" "8.0.x")
    JAVA_CACHE = [string](Get-OptionalProperty $Profile "JavaCache" "maven")
    JAVA_BUILD_COMMAND = [string](Get-OptionalProperty $Profile "JavaWorkflowBuild" "mvn -B test package")
    JAVA_ARTIFACT_DIRECTORY = [string](Get-OptionalProperty $Profile "JavaArtifactDirectory" "target")
    CMAKE_TARGET = [string](Get-OptionalProperty $Profile "CMakeTarget" "app")
    ANDROID_MODULE = [string]$Profile.BuildPath
    ELECTRON_BUILD_COMMAND = [string](Get-OptionalProperty $Profile "ElectronBuildCommand" "npm run dist")
    ELECTRON_OUTPUT = [string](Get-OptionalProperty $Profile "ElectronOutput" "dist")
    DOCKERFILE = [string]$Profile.BuildPath
    EXPECTED_ASSET = $expectedAssets[0]
    EXPECTED_ASSET_COMMENTS = (($expectedAssets | ForEach-Object { "# Expected asset: $_" }) -join "`n")
  }
  return [pscustomobject]@{
    Config = $config
    TemplateName = $templateName
    TemplateFile = Join-Path (Split-Path -Parent $PSScriptRoot) "assets\workflows\$($Profile.ProjectType).yml"
    Tokens = $tokens
    AdditionalFiles = @($additionalFiles)
  }
}

function Render-Template([string]$Path, [hashtable]$Tokens) {
  if (-not (Test-Path -LiteralPath $Path -PathType Leaf)) { throw "Workflow template not found: $Path" }
  $content = [IO.File]::ReadAllText($Path)
  foreach ($entry in $Tokens.GetEnumerator()) {
    $content = $content.Replace("{{$($entry.Key)}}", [string]$entry.Value)
  }
  if ($content -match '\{\{[A-Z][A-Z0-9_]*\}\}') {
    throw "Workflow template contains unresolved token: $($Matches[0])"
  }
  return $content
}

function Assert-ManagedConfigOrAbsent([string]$Path) {
  if (-not (Test-Path -LiteralPath $Path -PathType Leaf)) { return }
  $existing = Read-JsonFile $Path "Existing release config"
  $automation = Get-OptionalProperty $existing "automation"
  if (-not (Test-ManagedGeneratorName ([string](Get-OptionalProperty $automation "generator" "")))) {
    throw "Refusing to overwrite human-managed release config: $Path"
  }
}

function Test-ManagedGeneratorName([string]$Name) {
  return $Name -eq $script:GeneratorName -or $Name -in $script:LegacyGeneratorNames
}

function Test-ManagedWorkflowContent([string]$Content) {
  foreach ($marker in @($script:WorkflowMarker) + $script:LegacyWorkflowMarkers) {
    if ($Content.StartsWith($marker, [StringComparison]::Ordinal)) { return $true }
  }
  return $false
}

function Assert-ManagedWorkflowOrAbsent([string]$Path) {
  if (-not (Test-Path -LiteralPath $Path -PathType Leaf)) { return }
  $content = [IO.File]::ReadAllText($Path)
  if (-not (Test-ManagedWorkflowContent $content)) {
    throw "Refusing to overwrite human-managed workflow: $Path"
  }
}

function Test-ManagedWorkflow([string]$Path) {
  if (-not (Test-Path -LiteralPath $Path -PathType Leaf)) { return $false }
  return Test-ManagedWorkflowContent ([IO.File]::ReadAllText($Path))
}

function Assert-CompatibleWorkflowContent([string]$Content, [string]$ProjectType) {
  $workflowName = Get-WorkflowName $Content
  if ($Content -notmatch '(?ms)^on:\s*\r?\n\s+push:\s*\r?\n\s+tags:') {
    throw "Existing workflow is not compatible: tag push trigger is missing"
  }
  if ($Content -notmatch '(?ms)^permissions:\s*\r?\n\s+contents:\s*write\s*$') {
    throw "Existing workflow is not compatible: contents write permission is missing"
  }
  if ($Content -notmatch '(?i)(releaseDraft:\s*true|gh\s+release\s+create[^\r\n]*(?:\r?\n[^\r\n]*)*?--draft)') {
    throw "Existing workflow is not compatible: draft Release creation is missing"
  }
  if ($ProjectType -eq "docker") {
    if ($Content -notmatch '(?ms)^permissions:\s*.*?^\s+packages:\s*write\s*$') {
      throw "Existing Docker workflow is not compatible: packages write permission is missing"
    }
    if ($Content -notmatch 'docker/build-push-action@v7' -or $Content -notmatch '(?m)^\s+push:\s*true\s*$') {
      throw "Existing Docker workflow is not compatible: image push is missing"
    }
  }
  return $workflowName
}

function Write-AtomicUtf8([string]$Path, [string]$Content) {
  $directory = Split-Path -Parent $Path
  if (-not (Test-Path -LiteralPath $directory -PathType Container)) {
    New-Item -ItemType Directory -Force -Path $directory | Out-Null
  }
  $temporary = Join-Path $directory ((Split-Path -Leaf $Path) + ".tmp." + [guid]::NewGuid().ToString("N"))
  $backup = Join-Path $directory ((Split-Path -Leaf $Path) + ".bak." + [guid]::NewGuid().ToString("N"))
  try {
    [IO.File]::WriteAllText($temporary, $Content, [Text.UTF8Encoding]::new($false))
    if (Test-Path -LiteralPath $Path -PathType Leaf) {
      [IO.File]::Replace($temporary, $Path, $backup)
    }
    else {
      [IO.File]::Move($temporary, $Path)
    }
  }
  finally {
    if (Test-Path -LiteralPath $temporary) { Remove-Item -LiteralPath $temporary -Force }
    if (Test-Path -LiteralPath $backup) { Remove-Item -LiteralPath $backup -Force }
  }
}

function Invoke-Generate($Profile) {
  $configFile = Resolve-RepositoryFile $ConfigPath
  $selectedWorkflowPath = $WorkflowPath
  $workflowFile = Resolve-RepositoryFile $selectedWorkflowPath
  $reuseHumanWorkflow = $false
  $humanWorkflowName = $null

  if ((Test-Path -LiteralPath $workflowFile -PathType Leaf) -and -not (Test-ManagedWorkflow $workflowFile)) {
    if ($ExistingWorkflowPolicy -eq "CreateSeparate") {
      if ($SeparateWorkflowPath.Replace("\", "/") -eq $WorkflowPath.Replace("\", "/")) {
        throw "Separate workflow path must differ from the existing workflow path"
      }
      $selectedWorkflowPath = $SeparateWorkflowPath
      $workflowFile = Resolve-RepositoryFile $selectedWorkflowPath
      if ((Test-Path -LiteralPath $workflowFile -PathType Leaf) -and -not (Test-ManagedWorkflow $workflowFile)) {
        throw "Refusing to overwrite human-managed separate workflow: $workflowFile"
      }
      Write-Host "Preserving human workflow; using separate release workflow: $selectedWorkflowPath"
    }
    elseif ($ExistingWorkflowPolicy -eq "ReuseCompatible") {
      $humanWorkflowName = Assert-CompatibleWorkflowContent ([IO.File]::ReadAllText($workflowFile)) $Profile.ProjectType
      $reuseHumanWorkflow = $true
      Write-Host "Reusing compatible human-managed workflow: $selectedWorkflowPath"
    }
    else {
      throw "Refusing to overwrite human-managed workflow: $workflowFile"
    }
  }

  $bundle = New-GenerationBundle $Profile $selectedWorkflowPath (-not $reuseHumanWorkflow)
  if ($reuseHumanWorkflow) {
    $bundle.Config.publish.workflow.name = $humanWorkflowName
  }
  $configContent = ($bundle.Config | ConvertTo-Json -Depth 20) + "`n"
  $workflowContent = $null
  if (-not $reuseHumanWorkflow) {
    $workflowContent = Render-Template $bundle.TemplateFile $bundle.Tokens
    if (-not $workflowContent.EndsWith("`n")) { $workflowContent += "`n" }
  }

  Assert-ManagedConfigOrAbsent $configFile
  if (-not $reuseHumanWorkflow) {
    Assert-ManagedWorkflowOrAbsent $workflowFile
  }
  foreach ($additional in $bundle.AdditionalFiles) {
    $additionalPath = Resolve-RepositoryFile $additional.RelativePath
    if (
      (Test-Path -LiteralPath $additionalPath -PathType Leaf) -and
      [IO.File]::ReadAllText($additionalPath) -cne $additional.Content
    ) {
      throw "Refusing to overwrite existing generated support file: $additionalPath"
    }
  }

  $desired = @([pscustomobject]@{ Path = $configFile; Content = $configContent })
  if (-not $reuseHumanWorkflow) {
    $desired += [pscustomobject]@{ Path = $workflowFile; Content = $workflowContent }
  }
  foreach ($additional in $bundle.AdditionalFiles) {
    $desired += [pscustomobject]@{ Path = Resolve-RepositoryFile $additional.RelativePath; Content = $additional.Content }
  }
  foreach ($file in $desired) {
    if ((Test-Path -LiteralPath $file.Path -PathType Leaf) -and [IO.File]::ReadAllText($file.Path) -ceq $file.Content) {
      Write-Host "Unchanged: $($file.Path)"
    }
    else {
      Write-AtomicUtf8 $file.Path $file.Content
      Write-Host "Generated: $($file.Path)"
    }
  }
  Invoke-Validate
}

function Invoke-GenerateLocal($Profile) {
  $configFile = Resolve-RepositoryFile $ConfigPath
  $bundle = New-GenerationBundle $Profile $WorkflowPath $false
  $bundle.Config.automation | Add-Member -NotePropertyName localOnly -NotePropertyValue $true -Force
  $bundle.Config.publish.workflow = $null
  $bundle.Config.publish.release.mode = "none"
  $configContent = ($bundle.Config | ConvertTo-Json -Depth 20) + "`n"

  Assert-ManagedConfigOrAbsent $configFile
  foreach ($additional in $bundle.AdditionalFiles) {
    $additionalPath = Resolve-RepositoryFile $additional.RelativePath
    if (
      (Test-Path -LiteralPath $additionalPath -PathType Leaf) -and
      [IO.File]::ReadAllText($additionalPath) -cne $additional.Content
    ) {
      throw "Refusing to overwrite existing generated support file: $additionalPath"
    }
  }

  $desired = @([pscustomobject]@{ Path = $configFile; Content = $configContent })
  foreach ($additional in $bundle.AdditionalFiles) {
    $desired += [pscustomobject]@{ Path = Resolve-RepositoryFile $additional.RelativePath; Content = $additional.Content }
  }
  foreach ($file in $desired) {
    if ((Test-Path -LiteralPath $file.Path -PathType Leaf) -and [IO.File]::ReadAllText($file.Path) -ceq $file.Content) {
      Write-Host "Unchanged: $($file.Path)"
    }
    else {
      Write-AtomicUtf8 $file.Path $file.Content
      Write-Host "Generated local build config: $($file.Path)"
    }
  }
  Invoke-Validate
}

function Get-WorkflowName([string]$Content) {
  $match = [regex]::Match($Content, '(?m)^name:\s*(?<name>[^\r\n#]+)')
  if (-not $match.Success) { throw "Workflow does not declare a name" }
  return $match.Groups["name"].Value.Trim().Trim('"').Trim("'")
}

function Invoke-Validate {
  $configFile = Resolve-RepositoryFile $ConfigPath
  if (-not (Test-Path -LiteralPath $configFile -PathType Leaf)) { throw "Release config not found: $configFile" }
  $configText = [IO.File]::ReadAllText($configFile)
  if ($configText -match '\{\{[A-Z][A-Z0-9_]*\}\}') { throw "Release config contains unresolved token: $($Matches[0])" }
  $config = Read-JsonFile $configFile "Release config"
  $schemaVersion = [int](Get-RequiredProperty $config "schemaVersion" "root")
  if ($schemaVersion -notin @(1, 2)) { throw "Unsupported release config schemaVersion: $schemaVersion" }
  if ($schemaVersion -eq 2) {
    $configuredProjectType = [string](Get-RequiredProperty $config "projectType" "root")
    if ($configuredProjectType -notin @("tauri", "node", "go", "python", "rust", "dotnet", "java", "cmake", "flutter", "android", "electron", "docker")) { throw "Unsupported projectType: $configuredProjectType" }
  }
  foreach ($name in @("projectName", "branch", "remote")) { Get-RequiredProperty $config $name "root" | Out-Null }

  $version = Get-RequiredProperty $config "version" "root"
  $read = Get-RequiredProperty $version "read" "version"
  $readPath = [string](Get-RequiredProperty $read "path" "version.read")
  $readPattern = [string](Get-RequiredProperty $read "pattern" "version.read")
  try { $readRegex = [regex]::new($readPattern) } catch { throw "version.read.pattern is invalid: $($_.Exception.Message)" }
  if ($readRegex.GetGroupNames() -notcontains "version") { throw "version.read.pattern must contain a named 'version' capture group" }
  $versionFile = Resolve-RepositoryFile $readPath
  if (-not (Test-Path -LiteralPath $versionFile -PathType Leaf)) { throw "Version source not found: $versionFile" }
  $versionMatch = $readRegex.Match([IO.File]::ReadAllText($versionFile))
  if (-not $versionMatch.Success -or $versionMatch.Groups["version"].Value -notmatch '^\d+\.\d+\.\d+$') {
    throw "Version source does not match version.read.pattern: $readPath"
  }
  foreach ($update in @(Get-OptionalProperty $version "updates" @())) {
    $updatePath = [string](Get-RequiredProperty $update "path" "version.updates[]")
    $updateFile = Resolve-RepositoryFile $updatePath
    if (-not (Test-Path -LiteralPath $updateFile -PathType Leaf)) { throw "Version update file not found: $updateFile" }
    try { [regex]::new([string](Get-RequiredProperty $update "pattern" "version.updates[]")) | Out-Null }
    catch { throw "Invalid version update pattern for ${updatePath}: $($_.Exception.Message)" }
  }

  $publish = Get-RequiredProperty $config "publish" "root"
  $release = Get-RequiredProperty $publish "release" "publish"
  $releaseMode = [string](Get-RequiredProperty $release "mode" "publish.release")
  if ($releaseMode -notin @("publish-draft", "create", "none")) { throw "Unsupported publish.release.mode: $releaseMode" }
  $workflowConfig = Get-OptionalProperty $publish "workflow"
  $automation = Get-OptionalProperty $config "automation"
  $managed = [bool](Get-OptionalProperty $automation "managedWorkflow" $false)
  if ($schemaVersion -eq 2 -and $managed) {
    if (-not (Test-ManagedGeneratorName ([string](Get-OptionalProperty $automation "generator" "")))) { throw "Managed config generator is not recognized" }
    Get-RequiredProperty $automation "template" "automation" | Out-Null
    Get-RequiredProperty $automation "workflowFile" "automation" | Out-Null
  }

  if ($workflowConfig -or $managed) {
    $configuredWorkflowPath = [string](Get-OptionalProperty $automation "workflowFile" $WorkflowPath)
    $workflowFile = Resolve-RepositoryFile $configuredWorkflowPath
    if (-not (Test-Path -LiteralPath $workflowFile -PathType Leaf)) { throw "Release workflow not found: $workflowFile" }
    $workflow = [IO.File]::ReadAllText($workflowFile)
    if ($workflow -match '\{\{[A-Z][A-Z0-9_]*\}\}') { throw "Workflow contains unresolved token: $($Matches[0])" }
    if ($managed) {
      if (-not (Test-ManagedWorkflowContent $workflow)) { throw "Managed workflow marker is missing" }
      $template = [regex]::Escape([string]$automation.template)
      if ($workflow -notmatch "(?m)^# Template: $template\s*$") { throw "Managed workflow template does not match config" }
    }
    if ($workflowConfig) {
      $expectedWorkflowName = [string](Get-RequiredProperty $workflowConfig "name" "publish.workflow")
      if ((Get-WorkflowName $workflow) -ne $expectedWorkflowName) { throw "Workflow name does not match publish.workflow.name" }
    }
    if ($workflow -notmatch '(?ms)^on:\s*\r?\n\s+push:\s*\r?\n\s+tags:') { throw "Workflow must use a tag push trigger" }
    if ($workflow -notmatch '(?ms)^permissions:\s*\r?\n\s+contents:\s*write\s*$') { throw "Workflow must grant contents: write" }
    if ($workflow -notmatch '(?i)(releaseDraft:\s*true|gh\s+release\s+create[^\r\n]*(?:\r?\n[^\r\n]*)*?--draft)') {
      throw "Workflow must create a draft GitHub Release"
    }
    if ($configuredProjectType -eq "docker") {
      if ($workflow -notmatch '(?ms)^permissions:\s*.*?^\s+packages:\s*write\s*$') { throw "Docker workflow must grant packages: write" }
      if ($workflow -notmatch 'docker/build-push-action@v7' -or $workflow -notmatch '(?m)^\s+push:\s*true\s*$') {
        throw "Docker workflow must push the generated image"
      }
    }

    $expectedAssets = @([regex]::Matches($workflow, '(?m)^# Expected asset:\s*(?<name>\S.+?)\s*$') | ForEach-Object { $_.Groups["name"].Value })
    $requiredAssets = @(Get-OptionalProperty $release "requiredAssets" @())
    if ($managed -and $expectedAssets.Count -eq 0) { throw "Managed workflow does not declare expected assets" }
    if ($managed -and $requiredAssets.Count -eq 0) { throw "Managed config does not declare required assets" }
    $compiledPatterns = @()
    foreach ($asset in $requiredAssets) {
      $pattern = [string](Get-RequiredProperty $asset "pattern" "publish.release.requiredAssets[]")
      try { $compiledPatterns += [regex]::new($pattern) } catch { throw "Invalid required asset pattern: $pattern" }
    }
    if ($expectedAssets.Count -gt 0) {
      foreach ($name in $expectedAssets) {
        if (-not @($compiledPatterns | Where-Object { $_.IsMatch($name) }).Count) { throw "Expected workflow asset is not covered by config: $name" }
      }
      foreach ($pattern in $compiledPatterns) {
        if (-not @($expectedAssets | Where-Object { $pattern.IsMatch($_) }).Count) { throw "Required asset pattern does not match a generated asset: $($pattern.ToString())" }
      }
    }
  }
  Write-Host "Valid release automation: $configFile"
}

if (-not (Test-Path -LiteralPath $RepositoryRoot -PathType Container)) { throw "Repository root not found: $RepositoryRoot" }
$script:ResolvedRepositoryRoot = Get-NormalizedPath (Resolve-Path -LiteralPath $RepositoryRoot).Path

if ($Mode -eq "Validate") {
  Invoke-Validate
}
else {
  $profile = Get-ProjectProfile $ProjectType
  if ($Mode -eq "Detect") {
    [pscustomobject][ordered]@{
      projectType = $profile.ProjectType
      projectName = $profile.ProjectName
      version = $profile.Version
      versionSource = $profile.VersionSource
      packageManager = $profile.PackageManager
      buildPath = $profile.BuildPath
    } | ConvertTo-Json -Depth 4
  }
  elseif ($Mode -eq "GenerateLocal") {
    Invoke-GenerateLocal $profile
  }
  else {
    Invoke-Generate $profile
  }
}
